def drawbox(im, x, y, width):
    for i in range(0, width):
        im.putpixel( (x+i         , y+0         ), (0,0,0))
        im.putpixel( (x+i         , y+(width-1) ), (0,0,0))
        im.putpixel( (x+0         , y+i         ), (0,0,0))
        im.putpixel( (x+(width-1) , y+i         ), (0,0,0))
    
    return

